package _05_for_loops._2_badgers;

public class BadgerCode {
public static void main(String[] args) {
	//for
	for (int badgA =0; badgA<12;badgA ++ ) {
		System.out.print("badger, ");
	}
	System.out.println();
	
	for(int mushA =0; mushA <2;mushA ++) {
		System.out.print("mushroom, ");
	}
	System.out.println();
	System.out.println();
}
}
